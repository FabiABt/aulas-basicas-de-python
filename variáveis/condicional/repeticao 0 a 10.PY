#Em Python, os loops são codificados por meio dos comandos for e while
#O laço for nos permite percorrer os itens de uma coleção e, para cada um deles, 
# executar o bloco de código declarado no loop.
# while repete um bloco enqunato a condicão for verdadeira
x = 0
while x <= 10:
    print(x)
    x = x + 1
